<h1>Technical Implementation and System Requirements</h1>
<p>The iGiveTest system is developed in PHP 4 (PHP 5) programming language. The system depends on neither the operating system used on the Web-server, nor the type of server.</p>
<p>Only <strong>PHP</strong> 4.1.2 or higher versions are needed along with any widespread tool for database management (<strong>MySQL</strong>, <strong>PostgreSQL</strong>, <strong>MSSQL Server</strong> or <strong>Oracle</strong>).</p>
<p>The system can be tested in the following operating systems:</p>
<ul>
    <li>GNU/Linux (RedHat 6.x/7.x, Debian 2.x/3.x, Slackware 8.0,...) </li>
    <li>Windows 95/98/NT/2000/XP </li>
    <li>Solaris 8.0 </li>
    <li>FreeBSD 4.x </li>
</ul>
<p>The system supports most of the database management tools:</p>
<ul>
    <li>MySQL 3.22.32 and higher </li>
    <li>PostgreSQL 7.1.3 and higher </li>
    <li>MSSQL Server 2000 and higher </li>
    <li>Oracle 8 and higher </li>
</ul>
<p>For managing and working with an iGiveTest website, a browser of any type can be used. If you want to use the additional features of the HTML editor, you will need <strong>Internet Explorer 5.5</strong> (or higher), <strong>FireFox 1.0</strong> (or higher), or <strong>Mozilla 1.3</strong> (or higher) to ensure proper work of the editor.</p>

